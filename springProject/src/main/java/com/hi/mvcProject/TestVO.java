package com.hi.mvcProject;

public class TestVO {

	int idx;
	String day;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	@Override
	public String toString() {
		return "TestVO [idx=" + idx + ", day=" + day + "]";
	}
	
	
}
